#include "io.h"
// io提供向端口写入与获得相关值的函数
void init8259A(void){
	//你需要填写它
	//屏蔽所有中断源
	outb(0x21,0xff);
	outb(0xA1,0xFF);
	//主片初始化
    outb(0x20, 0x11);
    outb(0x21, 0x20);
    outb(0x21, 0x04);
    outb(0x21, 0x3);
	//从片初始化
    outb(0xA0, 0x11);
    outb(0xA1, 0x28);
    outb(0xA1, 0x02);
    outb(0xA1, 0x01);

}
